/**
  * @brief  SPI1 Speed setting function.
  * @param  speed : from SPI_BaudRate_Prescaler
  * @retval None.
  */
void spi1_set_speed(uint8_t speed)
{
    assert_param(IS_SPI_BAUDRATE_PRESCALER(speed)); /* Validity of judgment */
    __HAL_SPI_DISABLE(&hspi1);             			/* Disable SPI1 */
    hspi1.Instance->CR1 &= 0XFFC7;         			/* Bits 3-5 to clear, used to set the baud rate */
    hspi1.Instance->CR1 |= speed << 3;     			/* Set SPI speed */
    __HAL_SPI_ENABLE(&hspi1);              			/* Enable SPI */
}

/**
 * @brief       SPI1 reads and writes a byte of data
 * @param       txdata  : Data to be sent (1 byte)
 * @retval      Received data (1 byte)
 */
uint8_t spi1_read_write_byte(uint8_t txdata)
{
uint8_t rxdata;
HAL_SPI_TransmitReceive(&hspi1, &txdata, &rxdata, 1, 1000);
return rxdata; 		/* Returns the received data */
}

/**
  * @brief  Transmit an amount of data in blocking mode.
  * @param  tx_data: pointer to data buffer
  * @param  Size   : amount of data to be sent
  * @retval None.
  */
void spi1_write_data(uint8_t *tx_data, uint16_t size)
{
	HAL_SPI_Transmit(&hspi1, tx_data, size, HAL_MAX_DELAY);
}

